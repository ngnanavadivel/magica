package com.codemagic.magica.mapping.core;

public class Mapping {
	private String	target;
	private Node<?>	source;

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public Node<?> getSource() {
		return source;
	}

	public void setSource(Node<?> source) {
		this.source = source;
	}

}
